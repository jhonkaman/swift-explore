// Run: swift 09-loops/02-while.swift

// While loops repeat code as long as a condition is true.

var count = 1
while count <= 5 {
    print(count)
    count += 1
}

print("---")

var number = 10
while number > 0 {
    print(number)
    number -= 2
}

print("---")

var x = 1
while x <= 3 {
    print("Line \(x)")
    x += 1
}
