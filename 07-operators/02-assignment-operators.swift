// Assignment Operators

// Run: swift 07-operators/02-assignment-operators.swift

// Assignment operators assign values to variables.

var number = 10
print(number)

number = 20
print(number)

number += 5
print(number)

number -= 3
print(number)

number *= 2
print(number)

number /= 4
print(number)

number %= 3
print(number)
