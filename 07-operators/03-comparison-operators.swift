// Comparison operators compare two values and return true or false.

let x = 10
let y = 20

print(x == y)
print(x != y)
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)

let a = 5
let b = 5
print(a == b)
print(a >= b)
