// Else

// Run: swift 08-conditionals/02-else.swift

// The else statement executes code if the if condition is false.

let score = 45

if score >= 60 {
    print("Passed")
} else {
    print("Failed")
}

let number = 7

if number % 2 == 0 {
    print("Even")
} else {
    print("Odd")
}

let isRaining = false

if isRaining {
    print("Bring an umbrella")
} else {
    print("Enjoy the sunshine")
}
