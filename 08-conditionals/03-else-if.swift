// Else If

// Run: swift 08-conditionals/03-else-if.swift

// The else if statement checks additional conditions if previous ones are false.

let grade = 75

if grade >= 90 {
    print("A")
} else if grade >= 80 {
    print("B")
} else if grade >= 70 {
    print("C")
} else if grade >= 60 {
    print("D")
} else {
    print("F")
}

let day = 3

if day == 1 {
    print("Monday")
} else if day == 2 {
    print("Tuesday")
} else if day == 3 {
    print("Wednesday")
} else if day == 4 {
    print("Thursday")
} else if day == 5 {
    print("Friday")
} else {
    print("Weekend")
}
