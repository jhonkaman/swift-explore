// If

// Run: swift 08-conditionals/01-if.swift

// The if statement executes code if a condition is true.

let age = 20

if age >= 18 {
    print("You are an adult")
}

let temperature = 25

if temperature > 30 {
    print("It is hot")
}

if temperature < 0 {
    print("It is freezing")
}

if temperature >= 15 && temperature <= 25 {
    print("Perfect weather")
}
